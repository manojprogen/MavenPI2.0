package com.progen.timesetup;

import java.util.ArrayList;

/**
 *
 * @author Saurabh
 */
public class CalenderFormTable {

    private String calName;
    private String calId;
    private ArrayList calList;

    /**
     * @return the calName
     */
    public String getCalName() {
        return calName;
    }

    /**
     * @param calName the calName to set
     */
    public void setCalName(String calName) {
        this.calName = calName;
    }

    /**
     * @return the calId
     */
    public String getCalId() {
        return calId;
    }

    /**
     * @param calId the calId to set
     */
    public void setCalId(String calId) {
        this.calId = calId;
    }

    /**
     * @return the calList
     */
    public ArrayList getCalList() {
        return calList;
    }

    /**
     * @param calList the calList to set
     */
    public void setCalList(ArrayList calList) {
        this.calList = calList;
    }
}
